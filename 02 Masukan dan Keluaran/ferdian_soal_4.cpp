    cout<<"Hasil dari Penjumlahan"<<" "<<p<<" "<<"dengan"<<" "<<q<<" "<<"adalah"<<" "<<p + q<<endl;
    cout<<"Hasil dari Pengurangan"<<" "<<p<<" "<<"dengan"<<" "<<q<<" "<<"adalah"<<" "<<p - q<<endl;
